"""تطبيع النص العربي للبحث — حتى تتطابق «سماعه» مع «سماعة».

المشكلة: العربية تُكتب بأشكال متعددة لنفس الكلمة:
  سماعة / سماعه · أصلي / اصلي · مقلى / مقلي · مُكَيِّف / مكيف
لو بحثنا بالنص كما هو، «سماعه» لن تجد «سماعة» أبداً.

الحل: نطبّع الطرفين — نص المنتج يُخزَّن مطبَّعاً بعمود search_text،
وكلمة البحث تُطبَّع بنفس القواعد قبل المقارنة. تطابق مضمون.
"""

import re

# الحركات (التشكيل) والتطويل — تُحذف كلياً
_DIACRITICS = re.compile(r"[ً-ْـ]")

# توحيد الأحرف متعددة الأشكال
_REPLACEMENTS = str.maketrans({
    "أ": "ا", "إ": "ا", "آ": "ا",   # كل أشكال الألف → ا
    "ة": "ه",                        # التاء المربوطة → ه
    "ى": "ي",                        # الألف المقصورة → ي
    "ؤ": "و", "ئ": "ي",             # الهمزات على واو/ياء
})

# أرقام هندية → أسكي (المستخدم يكتب ٦٥ واط)
_ARABIC_DIGITS = str.maketrans("٠١٢٣٤٥٦٧٨٩", "0123456789")


def normalize(text):
    """يرجع نسخة مطبَّعة من النص، جاهزة للتخزين أو للمقارنة."""
    text = (text or "").strip().lower()          # اللاتيني يصير lowercase
    text = _DIACRITICS.sub("", text)
    text = text.translate(_REPLACEMENTS)
    text = text.translate(_ARABIC_DIGITS)
    text = re.sub(r"\s+", " ", text)             # فراغات متتالية → واحد
    return text
